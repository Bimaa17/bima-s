void main() {
  // String
  String firstName2 = "Bima"; 
  String lastName2 = "Setyawan"; 
  print(firstName2);  
  print(lastName2);   

  // Expression
  String firstName = "Bima"; 
  String lastName = "Setyawan"; 
  var fullName = "$firstName $lastName";  
  print(fullName);  

  // Menggabungkan String
  var name1 = firstName + " " + lastName;  
  var name2 = 'Bima' ' Setyawan';  
  print(name1);  
  print(name2);  
}
